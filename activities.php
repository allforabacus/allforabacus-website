					<div id="tabs-21">
						<p><h2>Cloud Computing Session by Eucalyptus</h2><br />
                  <div class="date">(21 July 2012, e-One Convention Centre)</div><br /><br />
                  <p>Talk conducted by Mr. Benno, one of the four representatives for Eucalyptus in India.( Eucalyptus is a California based company that undertakes open source cloud projects) </p>
                  <p><br />
                    <a href="http://www.eucalyptus.com/"><img src="images/activities/eucalyptus-340x36.png" width="340" height="36" alt="eucalyptus"></a></p>
                  <p><br />
                  </p>
                  <p>The session kicked off with an introduction to virtualization, the core concept behind Cloud Computing. The students were treated to a detailed presentation on the various virtualization options and their respective architectures which was all explained in a very easy manner by Mr. Benno. This topic received very enthusiastic response  in the form of numerous queries and doubts which were all cleared by Mr. Benno.</p>
                  <br />
                  <p>The afternoon had Mr. Benno taking a highly informative and highly detailed session on the starting steps for cloud computing and how the emergence of the cloud has drastically changed the scenario for start-ups looking for easy infrastructure. The cloud has also addressed the need for a portable and easily accessible storage space for many individuals and companies. He stressed on the various aspects that needed to be recognized and taken care of while developing cloud ready applications and emphasized on how the cloud was a next-gen tech that one should be able to incorporate even in traditional applications. Though Mr. Benno wanted to give a live demo on setting up a cloud, time constraints did not allow for it.</p><br />
                  <p>The seminar ended with Mr. Benno telling us how we could be part of Eucalyptus’ cloud through a number of projects that address various issues in cloud computing.</p>
